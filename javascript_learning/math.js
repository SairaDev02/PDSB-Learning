//* Constants
Math.E        // returns Euler's number
Math.PI       // returns PI
Math.SQRT2    // returns the square root of 2
Math.SQRT1_2  // returns the square root of 1/2
Math.LN2      // returns the natural logarithm of 2
Math.LN10     // returns the natural logarithm of 10
Math.LOG2E    // returns base 2 logarithm of E
Math.LOG10E   // returns base 10 logarithm of E

//* Methods
Math.round(x)   //returns x rounded TO its nearest integer
Math.ceil(x)    //returns x rounded UP to its nearest integer
Math.floor(x)   //returns x rounded DOWN to its nearest integer
Math.trunc(x)   //returns the integer part of x i.e 2.67 -> 2
Math.sign(x)    //returns if x is negative, null or positive
Math.pow(x, y)     //returns the value of x to the power of y
Math.sqrt(x)    //returns the square root of x
Math.abs(x)     //returns the absolute(positive) value of x
Math.sin(90 * Math.PI / 180);   //returns the sine (a value between -1 and 1) of the angle x (given in radians)
Math.cos(0 * Math.PI / 180);    //returns the cosine (a value between -1 and 1) of the angle x (given in radians)
Math.min(0, 150, 30, 20, -8, -200);     //finds lowest value
Math.max(0, 150, 30, 20, -8, -200);     //finds highest value
Math.random();  //returns a random number between 0 (inclusive) and 1 (exclusive)
Math.log(x);    //returns the natural logarithm of x
